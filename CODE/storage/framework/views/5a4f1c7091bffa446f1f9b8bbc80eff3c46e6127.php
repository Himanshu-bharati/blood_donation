<div class="page-header">   <h3>Why Donating ? </h3></div>
<p>
 Donating blood is safe and simple. It takes approximately 10-15 minutes to complete the blood donation process. Any healthy adult between 18 years and 60 years of age can donate blood. This is what you can expect when you are ready to donate blood:
</p>
<p>
    You walk into a reputed and safe blood donation centre or a mobile camp organized by a reputed institution.
    A few questions will be asked to determine your health status (general questions on health, donation history etc). Usually you will be asked to fill out a short form.
    </p>
    Then a quick physical check will be done to check temperature, blood pressure, pulse and hemoglobin content in blood to ensure you are a healthy donor.
    </p>
<p>
    If found fit to donate, then you will be asked to lie down on a resting chair or a bed. Your arm will be thoroughly cleaned. Then using sterile equipments blood will be collected in a special plastic bag. Approximately 350 ml of blood will be collected in one donation. Those who weigh more than 60 Kg can donate 450 ml of blood.
    Then you must rest and relax for a few minutes with a light snack and something refreshing to drink. Some snacks and juice will be provided.
        </p>
<p>
    Blood will be separated into components within eight hours of donation
    The blood will then be taken to the laboratory for testing.
    Once found safe, it will be kept in special storage and released when required.
    The blood is now ready to be taken to the hospital, to save lives.

    </p>

<div class="page-header">   <h3>Advantages of Donating</h3></div>
<p>
While donated blood is beneficial for people in need, some medical professionals maintain that donating blood also benefits the donor.
</p>




<div class="page-header">   <h4>Reducing Iron Levels</h4></div>
<p>
    Iron is a mineral that the body needs to produce red blood cells. However, too much iron can be harmful to a person's health. It can deposit into different organs of the body, such as the liver and heart, and affect the way those organs function.</p>



<div class="page-header">   <h4>Identifies adverse health effects</h4></div>

<p> Each person who donates blood completes a simply physical examination and short blood test before giving blood.


As a result, it is possible that a person could identify unknown health concerns as a part of the blood donation process. These health issues could include blood pressure concerns or low blood counts.</p>
<p>
  <br>
  
  A person should never use donating blood to find out if they have any blood-borne diseases, such as hepatitis B, hepatitis C, or HIV. Instead, they should always go to their doctor with any concerns they have.

Although low blood counts would stop a person from donating, discovering this information could help them pursue more information about their health.
</p>


<div class="page-header">   <h4>Helps people feel good about themselves</h4></div>

<p>
  
  Donating blood has the power to impact up to three people who need the blood to survive.<br>
  <br>


Knowing that they have made a difference to the lives of others can relieve a person's stress and make them feel mentally well or better about themselves than they did before.
</p>


<div class="page-header">   <h4>Burns Calories</h4></div>

<p>
  
  Donating blood burns up to 650 calories per donation, according to the St. Mary's Medical Center, Blue Springs, MO. While giving blood should not be an alternative to exercise, the calories burned might be seen as of occasional benefit by some.</p>
<br>
<p>In the right person, blood donation offers many health benefits with few risks. Blood banks usually take every precaution possible to help a blood donor give their blood safely.
</p>


